/**
 * Framework Logos Component
 * Displays logo grid for all supported frameworks with selection
 */
export function FrameworkLogos({
  selected = 'nextjs',
  onSelect,
  className,
  'data-testid': testId = 'framework-logos',
}: FrameworkLogosProps) {
  const shouldReduceMotion = useReducedMotion();
  const reducedMotion = shouldReduceMotion ?? false;

  return (
    <div
      className={cn('flex flex-wrap justify-center gap-3', className)}
      data-testid={testId}
    >
      {frameworks.map((framework) => (
        <motion.button
          key={framework.id}
          onClick={() => onSelect?.(framework.id)}
          whileHover={reducedMotion ? {} : { scale: 1.05 }}
          whileTap={reducedMotion ? {} : { scale: 0.95 }}
          className={cn(
            'flex items-center gap-2 px-4 py-2 rounded-lg border transition-colors',
            selected === framework.id
              ? 'bg-primary/10 border-primary text-primary'
              : 'bg-white dark:bg-neutral-800 border-neutral-200 dark:border-neutral-700 text-neutral-600 dark:text-neutral-400 hover:border-primary/50'
          )}
          data-testid={`framework-${framework.id}`}
          aria-pressed={selected === framework.id}
        >
          <span style={{ color: selected === framework.id ? undefined : framework.color }}>
            {framework.icon}
          </span>
          <span className="text-sm font-medium">{framework.name}</span>
        </motion.button>
      ))}
    </div>
  );
}

export default FrameworkLogos;
